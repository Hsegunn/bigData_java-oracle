SELECT * FROM EMP WHERE JOB IN ('MANAGER','CLERK','SALESMAN');
--사원번호 7566,7782,7934인 사원을 제외한 사원번호, 급여, 입사일 조회
SELECT ENAME, HIREDATE, SAL FROM EMP WHERE 
EMPNO NOT IN (7566,7782,7934);
--부서번호 30에서 근무하고 월 2000달러 이하를 받는 81년 5월 1일 이전에 입사한 사원이름, 
--급여, 부서번호, 입사일 조회
SELECT ENAME, DEPTNO , HIREDATE, SAL FROM EMP WHERE DEPTNO IN (30) 
AND SAL <= 2000 AND HIREDATE BETWEEN TO_DATE('19800101','YYYYMMDD')
AND TO_DATE('19810501','YYYYMMDD');
--부서번호:10또는30, 월급:2000~5000 의 사원명, 급여, 부서번호
SELECT ENAME, DEPTNO, SAL FROM EMP WHERE DEPTNO IN (10, 30) AND
SAL BETWEEN 2000 AND 5000;
--직업: MANAGER OR SALESMAN 급여: 1600,2975,2850가 아닌 사원정보
SELECT ENAME, JOB, SAL, HIREDATE  FROM EMP WHERE 
JOB IN ('MANAGER', 'SALESMAN') AND SAL NOT IN (1600,2975,2850);
