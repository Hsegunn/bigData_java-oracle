--81년 5월 1일과 81년 12월 3일 사이에 입사한 사원, 급여, 입사일 조회
SELECT ENAME, HIREDATE, SAL FROM EMP WHERE HIREDATE BETWEEN
TO_DATE('19810501','YYYYMMDD') AND TO_DATE('19811203','YYYYMMDD');
--87년도 입사한 사원, 급여, 입사일 조회
SELECT ENAME, HIREDATE, SAL FROM EMP WHERE HIREDATE BETWEEN
TO_DATE('19870101','YYYYMMDD') AND TO_DATE('19871231','YYYYMMDD');
--직업이 MANAGER, 'CLERK', 'SALESMAN'
SELECT * FROM EMP WHERE JOB = 'MANAGER' OR JOB = 'CLERK'OR
